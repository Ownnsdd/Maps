package com.xb.maps;

public class Friend {
	private long id;
	private String name;
	private String phone;

	public long getID() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getPhone() {
		return phone;
	}

	public void setID(long id) {
		this.id = id;
	}

	public void setName(String Name) {
		this.name = Name;
	}

	public void setPhone(String Phone) {
		this.phone = Phone;
	}
}
